# Solicitar al usuario el valor del lado del cuadrado
lado = float(input("Ingrese el valor del lado del cuadrado: "))

# Calcular el área y el perímetro del cuadrado
area = lado ** 2
perimetro = 4 * lado

# Mostrar el área y el perímetro por pantalla
print(f"El área del cuadrado es: {area}")
print(f"El perímetro del cuadrado es: {perimetro}")
