# Solicitar al usuario ingresar dos números enteros
var_1 = int(input("Ingresa el primer número entero: "))
var_2 = int(input("Ingresa el segundo número entero: "))

# Realiza la suma de los dos números
suma = var_1 + var_2

# Muestra el resultado
print(f"La suma de {var_1} y {var_2} es igual a {suma}")

