# Solicitar al usuario la longitud del lado del cubo
lado = float(input("Ingrese la longitud del lado del cubo: "))

# Calcular el 치rea del cubo (치rea de la superficie)
area = 6 * (lado ** 2)

# Calcular el volumen del cubo
volumen = lado ** 3

# Mostrar el 치rea y el volumen por pantalla
print(f"El 치rea del cubo es: {area}")
print(f"El volumen del cubo es: {volumen}")
